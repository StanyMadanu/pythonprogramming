# A Python program to Swap values
print("Swapping Values")
a=input("Enter first value:")
b=input("Enter Second Value:")

print("---------------------------")
print("First original value is = {}".format(a))
print("Second original value is = {}".format(b))
print("---------------------------")

#2nd swapping syntax
a,b = b, a

print("1st Swapped value = {}".format(a))
print("2nd Swapped value = {}".format(b))
print("=====================================")

