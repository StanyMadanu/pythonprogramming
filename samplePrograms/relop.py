#Demonstrating Relational Operators

#initialising values
a = int(input("Enter 'a' Value:"))
b = int(input("Enter 'b' Value:"))

#output
print("------------------------------------------------")
print("\tRelational Operators")
print("------------------------------------------------")

print("\t{} > {} = {}".format(a, b, a>b))
print("\t{} < {} = {}".format(a, b, a<b))
print("\t{} == {} = {}".format(a, b, a==b))
print("\t{} != {} = {}".format(a, b, a!=b))
print("\t{} >= {} = {}".format(a, b, a>=b))
print("\t{} <= {} = {}".format(a, b, a<=b))
print("------------------------------------------------")
