#Arithmetic Operators

a=int(input("Enter a value:"))
b=int(input("Enter b value:"))

print("=====================================================================")
print("\tArithmetic Operators")
print("=====================================================================")
print("\tAddition of {} and {} = {}".format(a,b, a+b))
print("\tSubtraction of {} and {} = {}".format(a, b, a-b))
print("\tMultiplication of {} and {} = {}".format(a, b, a*b))
print("\tDivision(quotient) of {} and {} = {}".format(a, b, a/b))
print("\tDivision Reminder of {} and {} = {}".format(a, b, a%b))
print("\tInteger div_quotient of {} and {} = {}".format(a, b, a//b))
print("\tExponentation (power) of {} and {} = {}".format(a, b, a**b))
print("=====================================================================")

